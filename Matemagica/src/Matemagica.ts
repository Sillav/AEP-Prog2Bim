class Matemagica {

    public soma(num1: number, num2: number) {
        return num1 + num2;
    }

    public subtracao(num1: number, num2: number) {
        return num1 - num2;
    }

    public multiplicacao(num1: number, num2: number) {
        return num1*num2;
    }

    public divisao(num1: number, num2: number) {
        return num1/num2;
    }

}

export default new Matemagica()